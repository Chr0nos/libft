#include "libft.h"

char	*ft_strtoupper(char *s)
{
	unsigned int	p;

	p = 0;
	while (s[p])
	{
		if ((s[p] >= 'a') && (s[p] <= 'z'))
			s[p] = s[p] - 'a' + 'A';
		p++;
	}
	return (s);
}

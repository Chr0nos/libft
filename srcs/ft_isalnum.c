int		ft_isalnum(const char *str)
{
	while ((*str) && (((*str >= 'A') && (*str <= 'Z')) ||
		((*str >= 'a') && (*str <= 'z')) ||
			((*str >= '0') && (*str <= '9'))))
		str++;
	return ((*str ? 0 : 1));
}

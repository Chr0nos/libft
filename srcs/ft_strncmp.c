#include "unistd.h"

int		ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t	p;

	p = 0;
	while ((s1[p] != '\0') && (s1[p] == s2[p]) && (n--))
		p++;
	return (s1[p] - s2[p]);
}

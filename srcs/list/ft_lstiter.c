#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	t_list	*x;

	if (!lst)
		ft_crash();
	x = lst;
	while (x)
	{
		f(x);
		x = x->next;
	}
}
